package com.ita.if103java.ims.mapper.dto;

import com.ita.if103java.ims.dto.AssociateDto;
import com.ita.if103java.ims.entity.Associate;
import com.ita.if103java.ims.entity.AssociateType;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;

import static org.junit.jupiter.api.Assertions.assertEquals;

class AssociateDtoMapperTest {
    private Associate associate = new Associate(1L, 1L, "Associate name", "test@test.com",
        "+380977959707", "additionalInfo", AssociateType.SUPPLIER, true);

    private AssociateDto associateDto = new AssociateDto(associate.getId(), associate.getAccountId(), associate.getName(),
        associate.getEmail(), associate.getPhone(), associate.getAdditionalInfo(), associate.getType(), true, null);

    private AssociateDtoMapper associateDtoMapper = new AssociateDtoMapper();

    @Test
    void toEntity() {
        assertEquals(associate, associateDtoMapper.toEntity(associateDto));
    }

    @Test
    void toDto() {
        assertEquals(associateDto, associateDtoMapper.toDto(associate));
    }
}
